package team.carrypigeon.backend.chat.domain.shared.domain.problem;

/**
 * 统一业务问题类型。
 * 职责：为协议层与应用层之间建立稳定的问题分类。
 * 边界：这里只表达当前阶段需要的最小问题类型，不扩展复杂错误码体系。
 */
public enum ProblemType {
    /**
     * 输入或业务前置条件不满足。
     * 适用：请求字段非法、业务参数越界、验证码错误、游标格式错误等调用方可修正的问题。
     * 边界：不用于表达权限不足、资源不存在或服务内部失败。
     */
    VALIDATION,

    /**
     * 当前资源状态与目标操作冲突。
     * 适用：重复创建、重复处理、状态已结束或同一业务对象不能重复变更的场景。
     * 边界：不用于表达普通字段校验失败。
     */
    CONFLICT,

    /**
     * 当前调用方无权执行目标操作。
     * 适用：未认证、凭证无效、成员身份不足、角色层级不足或资源访问被拒绝。
     * 边界：认证失败和授权失败共享该类型，具体原因由 {@code reason} 区分。
     */
    FORBIDDEN,

    /**
     * 目标业务资源不存在。
     * 适用：账号、频道、消息、文件或其它领域对象不存在，且调用方可感知该缺失。
     * 边界：不暴露底层存储异常或内部对象键细节。
     */
    NOT_FOUND,

    /**
     * 服务内部能力不可用或业务流程无法完成。
     * 适用：外部能力缺失、投递失败、存储不可用等调用方无法通过改请求自行修复的问题。
     * 边界：不替代基础设施异常，进入协议响应前应由业务入口收敛为稳定 reason。
     */
    INTERNAL
}
